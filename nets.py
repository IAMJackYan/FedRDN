def vgg11():
    """VGG 11-layer model (configuration "A") with batch normalization"""
    return VGG(make_layers(cfg['A'], batch_norm=True))


def vgg11_fedclip():
    """VGG 11-layer model (configuration "A") with batch normalization"""
    return VGG_fedclip(make_layers(cfg['A'], batch_norm=True))

